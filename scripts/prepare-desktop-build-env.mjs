#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";

const DEFAULT_ENV_FILE = process.env.DOYA_DESKTOP_ENV_FILE?.trim() || "docker/.env";
const OUTPUT_ENV_FILE = ".desktop-build.env";
const OUTPUT_RUNTIME_ENV_FILE = "packages/desktop/generated/daemon-env.json";

const PUBLIC_ENV_MAPPINGS = [
  {
    output: "EXPO_PUBLIC_CONTROL_API_URL",
    inputs: [
      "EXPO_PUBLIC_CONTROL_API_URL",
      "DOYA_PUBLIC_CONTROL_API_URL",
      "DOYA_CONTROL_AI_GATEWAY_PUBLIC_BASE_URL",
    ],
  },
  {
    output: "EXPO_PUBLIC_LOCAL_DAEMON",
    inputs: ["EXPO_PUBLIC_LOCAL_DAEMON", "DOYA_PUBLIC_DAEMON"],
  },
  {
    output: "EXPO_PUBLIC_ONLYOFFICE_DOCUMENT_SERVER_URL",
    inputs: ["EXPO_PUBLIC_ONLYOFFICE_DOCUMENT_SERVER_URL"],
  },
];

const RUNTIME_DAEMON_ENV_KEYS = [
  "DOYA_RELAY_ENABLED",
  "DOYA_RELAY_ENDPOINT",
  "DOYA_RELAY_PUBLIC_ENDPOINT",
  "DOYA_RELAY_USE_TLS",
  "DOYA_RELAY_PUBLIC_USE_TLS",
  "DOYA_CONTROL_DAEMON_ENDPOINT",
  "DOYA_CONTROL_DAEMON_PUBLIC_ENDPOINT",
  "DOYA_CONTROL_HEARTBEAT_INTERVAL_MS",
  "DOYA_CORS_ORIGINS",
  "DOYA_HOSTNAMES",
];

const FORBIDDEN_RUNTIME_ENV_KEYS = new Set([
  "DOYA_CONTROL_TOKEN",
  "DOYA_CONTROL_NODE_REGISTRATION_TOKEN",
  "DOYA_CONTROL_RUNTIME_AUTH_TOKEN",
  "DOYA_SMS_ACCOUNT",
  "DOYA_SMS_PASSWORD",
  "DOYA_PAYMENT_MERCHANT_KEY",
  "DOYA_CONTROL_AI_GATEWAY_UPSTREAM_API_KEY",
]);

function parseArgs(argv) {
  const args = { envFile: DEFAULT_ENV_FILE };
  for (let index = 2; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--env-file") {
      args.envFile = argv[++index];
      continue;
    }
    throw new Error(`Unknown argument: ${arg}`);
  }
  return args;
}

function parseEnvFile(filePath) {
  if (!existsSync(filePath)) {
    return {};
  }

  const values = {};
  const content = readFileSync(filePath, "utf8");
  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) {
      continue;
    }

    const equalsIndex = line.indexOf("=");
    if (equalsIndex === -1) {
      continue;
    }

    const key = line.slice(0, equalsIndex).trim();
    const value = unquoteEnvValue(line.slice(equalsIndex + 1).trim());
    values[key] = value;
  }
  return values;
}

function unquoteEnvValue(value) {
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    return value.slice(1, -1);
  }
  return value;
}

function firstNonEmpty(env, keys) {
  for (const key of keys) {
    const value = env[key]?.trim();
    if (value) {
      return value;
    }
  }
  return null;
}

function shellQuote(value) {
  return `'${value.replaceAll("'", "'\\''")}'`;
}

function buildPublicEnv(env) {
  const output = {};
  for (const mapping of PUBLIC_ENV_MAPPINGS) {
    const value = firstNonEmpty(env, mapping.inputs);
    if (value) {
      output[mapping.output] = value;
    }
  }
  return output;
}

function buildRuntimeDaemonEnv(env) {
  const output = {};
  for (const key of RUNTIME_DAEMON_ENV_KEYS) {
    if (FORBIDDEN_RUNTIME_ENV_KEYS.has(key)) {
      throw new Error(`Refusing to include forbidden desktop runtime env key: ${key}`);
    }
    const value = env[key]?.trim();
    if (value) {
      output[key] = value;
    }
  }
  return output;
}

function writeBuildEnvFile(values) {
  const lines = [
    "# Generated by scripts/prepare-desktop-build-env.mjs.",
    "# Do not commit this file.",
    ...Object.entries(values).map(([key, value]) => `export ${key}=${shellQuote(value)}`),
    "",
  ];
  writeFileSync(OUTPUT_ENV_FILE, lines.join("\n"));
}

function writeRuntimeEnvFile(values) {
  mkdirSync(path.dirname(OUTPUT_RUNTIME_ENV_FILE), { recursive: true });
  writeFileSync(`${OUTPUT_RUNTIME_ENV_FILE}`, `${JSON.stringify(values, null, 2)}\n`);
}

function main() {
  const args = parseArgs(process.argv);
  const fileEnv = parseEnvFile(args.envFile);
  const env = { ...fileEnv, ...process.env };
  const publicEnv = buildPublicEnv(env);
  const runtimeEnv = buildRuntimeDaemonEnv(env);

  writeBuildEnvFile(publicEnv);
  writeRuntimeEnvFile(runtimeEnv);

  const publicKeys = Object.keys(publicEnv).sort();
  const runtimeKeys = Object.keys(runtimeEnv).sort();
  console.log(
    [
      `Prepared desktop build env from ${args.envFile}.`,
      `Public build keys: ${publicKeys.length ? publicKeys.join(", ") : "(none)"}.`,
      `Daemon runtime keys: ${runtimeKeys.length ? runtimeKeys.join(", ") : "(none)"}.`,
    ].join("\n"),
  );
}

main();
